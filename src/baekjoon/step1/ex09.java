package baekjoon.step1;

import java.util.Scanner;

public class ex09 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int first = sc.nextInt();
		int second = sc.nextInt();
		
		System.out.println(first+second);
		System.out.println(first-second);
		System.out.println(first*second);
		System.out.println(first/second);
		System.out.println(first%second);
	}

}
